function randomItem(values) {
  if (!Array.isArray(values) || values.length === 0) {
    return '';
  }
  const index = Math.floor(Math.random() * values.length);
  return values[index];
}

function providerLabel(providerId, providers) {
  const provider = (providers || []).find((entry) => entry.id === providerId);
  return (provider && provider.name) || 'AI';
}

function escapeRegExp(value) {
  return String(value).replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

export function generateFriendlyConnectorName(providerId, providers) {
  const label = providerLabel(providerId, providers);
  const adjective = randomItem(FRIENDLY_ADJECTIVES);
  const noun = randomItem(FRIENDLY_NOUNS);

  if (!adjective && !noun) {
    return `${label} Connector`;
  }
  if (!noun) {
    return `${label} ${adjective}`;
  }
  if (!adjective) {
    return `${label} ${noun}`;
  }
  return `${label} ${adjective} ${noun}`;
}

export function getFriendlyNamePlaceholder(providerId, providers) {
  const label = providerLabel(providerId, providers);
  return `e.g. ${label} Bright Harbor`;
}

export function isLikelyAutoGeneratedName(name, providers) {
  const trimmed = (name || '').trim();
  if (!trimmed) {
    return true;
  }

  const lower = trimmed.toLowerCase();
  for (const provider of providers || []) {
    const providerName = (provider.name || '').trim();
    if (!providerName) {
      continue;
    }

    const lowerProviderName = providerName.toLowerCase();
    if (lower === `${lowerProviderName} connector`) {
      return true;
    }

    // git-lrc smart format: "Provider Adjective Noun"
    const providerPrefix = `${lowerProviderName} `;
    if (lower.startsWith(providerPrefix)) {
      const remainder = trimmed.slice(providerPrefix.length);
      const parts = remainder.split(/\s+/);
      if (parts.length === 2) {
        const [adj, noun] = parts;
        if (FRIENDLY_ADJECTIVES.includes(adj) && FRIENDLY_NOUNS.includes(noun)) {
          return true;
        }
      }
    }

    // LiveReview legacy style: "Provider-Adjective123"
    const safeProviderName = escapeRegExp(providerName);
    const liveReviewPattern = new RegExp(`^${safeProviderName}-[A-Za-z]+\\d{1,3}$`, 'i');
    if (liveReviewPattern.test(trimmed)) {
      return true;
    }
  }

  return false;
}
